/** Nerd Font glyph for a directory entry, by name and kind. */
export function fileIconGlyph(name: string, isDirectory: boolean, expanded = false): string {
  if (isDirectory) return expanded ? FOLDER_OPEN : FOLDER;

  const lower = name.toLowerCase();
  const byName = ICON_BY_FILENAME[lower];
  if (byName) return byName;

  // Try the longest dotted suffix first (e.g. "d.ts" before "ts").
  let dot = lower.indexOf('.');
  while (dot !== -1) {
    const ext = lower.slice(dot + 1);
    const byExt = ICON_BY_EXTENSION[ext];
    if (byExt) return byExt;
    dot = lower.indexOf('.', dot + 1);
  }

  return DEFAULT_FILE;
}
